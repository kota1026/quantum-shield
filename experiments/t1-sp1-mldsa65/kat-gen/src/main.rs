// Generate ML-DSA-65 KAT vector (pk, msg, sig) and emit as Rust byte arrays.
use fips204::ml_dsa_65;
use fips204::traits::{KeyGen, SerDes, Signer, Verifier};

fn main() {
    let mut rng = rand_core::OsRng;
    let (pk, sk) = ml_dsa_65::try_keygen_with_rng(&mut rng).expect("keygen failed");

    let msg = b"quantum-shield-t1-test-message";
    let ctx: &[u8] = b"";
    let sig = sk
        .try_sign_with_rng(&mut rng, msg, ctx)
        .expect("sign failed");

    let pk_bytes = pk.into_bytes();
    let sig_bytes = sig;

    // Sanity verify
    let pk2 = ml_dsa_65::PublicKey::try_from_bytes(pk_bytes).unwrap();
    let ok = pk2.verify(msg, &sig_bytes, ctx);
    eprintln!("local verify ok: {}", ok);
    assert!(ok);

    println!("// ML-DSA-65 KAT generated by kat-gen");
    println!("pub const MSG: &[u8] = b\"quantum-shield-t1-test-message\";");
    println!("pub const PK_LEN: usize = {};", pk_bytes.len());
    println!("pub const SIG_LEN: usize = {};", sig_bytes.len());
    println!("pub const PK: [u8; {}] = {:?};", pk_bytes.len(), pk_bytes);
    println!("pub const SIG: [u8; {}] = {:?};", sig_bytes.len(), sig_bytes);
}
