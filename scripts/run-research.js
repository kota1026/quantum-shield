const https = require('https');
const fs = require('fs');

const API_KEY = process.env.ANTHROPIC_API_KEY;
const SLACK_URL = process.env.SLACK_WEBHOOK_URL;

if (!API_KEY) {
  console.log('ANTHROPIC_API_KEY not set — skipping');
  process.exit(0);
}

const DATE = new Date().toISOString().split('T')[0];

const prompt = `You are a PQC research analyst for Quantum Shield, an Ethereum PQC custody protocol using ML-DSA-65 + SPHINCS+. Generate a weekly intelligence report for ${DATE}.

Cover:
1. NIST PQC standards updates (FIPS 204/205/203)
2. Ethereum Foundation PQC efforts (EIPs, research, Vitalik statements)
3. Competitor activity (QRL, PQShield, other PQC blockchain projects)
4. Quantum computing hardware milestones (IBM, Google, Microsoft)
5. Relevant academic papers
6. Strategic recommendations for Quantum Shield

Format as Markdown with ## headers. Be specific and actionable.`;

const body = JSON.stringify({
  model: 'claude-sonnet-4-6',
  max_tokens: 8192,
  messages: [{ role: 'user', content: prompt }],
});

const req = https.request('https://api.anthropic.com/v1/messages', {
  method: 'POST',
  headers: {
    'content-type': 'application/json',
    'x-api-key': API_KEY,
    'anthropic-version': '2023-06-01',
  },
}, (res) => {
  let data = '';
  res.on('data', (chunk) => data += chunk);
  res.on('end', () => {
    try {
      const parsed = JSON.parse(data);
      const report = parsed.content?.[0]?.text || 'API call failed';

      // Write report
      fs.mkdirSync('docs/intelligence', { recursive: true });
      const content = `# PQC Weekly Intelligence - ${DATE}\n\n_Auto-generated by Claude API_\n\n${report}\n`;
      fs.writeFileSync(`docs/intelligence/${DATE}-weekly.md`, content);
      fs.writeFileSync('docs/intelligence/LATEST.md', content);
      console.log(`Report written: docs/intelligence/${DATE}-weekly.md`);

      // Send to Slack
      if (SLACK_URL) {
        const summary = report.split('\n').slice(0, 15).join('\n');
        const slackBody = JSON.stringify({
          text: `*Weekly PQC Intelligence - ${DATE}*\n\n${summary}\n\nFull report: https://github.com/kota1026/quantum-shield/pulls`,
        });
        const slackReq = https.request(SLACK_URL, {
          method: 'POST',
          headers: { 'Content-Type': 'application/json' },
        });
        slackReq.write(slackBody);
        slackReq.end();
        console.log('Slack notification sent');
      }
    } catch (e) {
      console.error('Parse error:', e.message);
      process.exit(1);
    }
  });
});

req.write(body);
req.end();
